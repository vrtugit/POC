package com.bluzsmart.swagger.swaggerexample.domain;

public class User {

	private String name;
	private Long salary;

	public User(String name, Long salary) {
		super();
		this.name = name;
		this.salary = salary;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Long getSalary() {
		return salary;
	}

	public void setSalary(Long salary) {
		this.salary = salary;
	}

}
